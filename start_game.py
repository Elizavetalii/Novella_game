def play_game():
    print("Добро пожаловать в игру 'Спаси принцессу из замка или погибни в осиновом лесу'!\n\n"
        "Вокруг вас лес. Вы находитесь в нескольких милях от замка, где вас ждет принцесса."
        " Она была заточена в этом замке своей мачехой, которая знает про сверхспособности принцессы и силой удерживает ее в замке."
        " Злобная мачеха с помощью своего осинового кола превратила всех лесных жителей в злодеев, чтобы принцесса уж точно не сбежала"
        " и никто не смог подобраться к замку. Вы готовы на риск ради спасения прекрасной принцессы?\n"
        "На вашем пути будут встречаться злодеи, с которыми вы должны побороться ради нее.\n"
        "Не переживайте! У вас в инвентаре есть все, чтобы справиться со злодеями.\n"
        "Вам предстоит истребить 4 вида персонажей, чтобы добраться до замка.\n"
        "Мы в вас верим!\n" 
        "Если вы выберите предмет, которого нет в инвентаре - игра, к сожалению, закончится.")
    
    princess_saved = False

    while not princess_saved and len(characters_encountered) < 4:

        enemy = random.choice(["леший", "фея", "дракон", "мачеха"])

        if enemy not in characters_encountered:
            characters_encountered[enemy] = True
        
        if enemy == "леший":
            encounter_leshy()
        elif enemy == "фея":
            encounter_fairy()
        elif enemy == "дракон":
            encounter_dragon()
        elif enemy == "мачеха":
            encounter_mather()

        if len(characters_encountered) < 4:
            print(f"Вы встретили {len(characters_encountered)} из 4 видов персонажей.")

        if len(characters_encountered) == 4:
            princess_saved = True
            print("\nЭто был долгий и тернистый путь. Но вот вы и добрались до принцессы и спасли ее!!")

    print("\nТы лучший герой! Спасибо за игру!")

play_game()